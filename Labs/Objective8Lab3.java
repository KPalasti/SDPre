public class Objective8Lab3 {
  public static void main(String[] args) {

    int max =21;
    int counter;

    for(counter=1;counter<max;counter++){
      if(counter%2==0){
        System.out.println(counter+" is even");
      }
      else{
        System.out.println(counter+" is odd");
      }

    }
  }
}
